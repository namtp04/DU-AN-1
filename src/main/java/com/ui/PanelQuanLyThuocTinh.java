    private <E extends ThuocTinh> void filltable(DAOInterface dao, E entity) {
        try {
            model.setRowCount(0);
            List<E> list = dao.selectAll();
            for (E e : list) {
                model.addRow(new Object[]{e.getMa(), e.getTen()});
            }
        } catch (Exception e) {
        }
    }

